public class Lingkaran {
    double hitungLuas(int jejari){
        return 3.14 * jejari * jejari;
    }
    
    double hitungLuas(double jejari){
        return 3.14 * jejari * jejari;
    }
    
    double hitungKeliling(int jejari){
        return 2 * 3.14 * jejari;
    }
    
    double hitungKeliling(double jejari){
        return 2 * 3.14 * jejari;
    }
}
